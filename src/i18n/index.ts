import { create } from 'zustand';
import { persist } from 'zustand/middleware';

export type Language = 'en' | 'de' | 'es';

interface I18nState {
  language: Language;
  setLanguage: (l: Language) => void;
}

export const useLanguage = create<I18nState>()(
  persist(
    (set) => ({
      language: 'en',
      setLanguage: (language) => set({ language }),
    }),
    { name: 'c40k_language' }
  )
);

